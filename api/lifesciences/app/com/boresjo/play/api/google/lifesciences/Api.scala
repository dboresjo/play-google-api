				}
				object run {
					def apply(projectsId :PlayApi, locationsId :PlayApi, parent: String)(using signer: RequestSigner, ec: ExecutionContext): run = new run(ws.url(BASE_URL + s"v2beta/projects/${projectsId}/locations/${locationsId}/pipelines:run").addQueryStringParameters("parent" -> parent.toString))
				}
			}
		}
	}
}
